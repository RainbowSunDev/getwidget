/// [GFTextFieldShape] is used to shape the GFIconButton widget.
enum GFTextFieldShape {
  /// [GFTextFieldShape.roundedsquare], TextField with rounded square edges
  roundedsquare,

  /// [GFTextFieldShape.pills], used for TextField with curved edges
  pills,

  /// [GFTextFieldShape.square], used for TextField with squared edges
  square,
}
