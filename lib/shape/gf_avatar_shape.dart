/// [GFAvatarShape] is used to shape the GFAvatar widget.
enum GFAvatarShape {
  /// Default shape is [GFAvatarShape.circle], used for rounded avatar
  circle,

  /// [GFAvatarShape.standard], used for square avatar with rounded corners
  standard,

  /// [GFAvatarShape.square], used for square avatar
  square,
}
