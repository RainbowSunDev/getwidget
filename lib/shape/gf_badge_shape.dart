/// [GFBadgeShape] is used to shape the GFBadge widget.
enum GFBadgeShape {
  /// [GFBadgeShape.circle], used for rounded shape badge
  circle,

  /// [GFBadgeShape.pills], used for pills shaped badge with rounded corners
  pills,

  /// Default shape is [GFBadgeShape.standard], used for standard rectangle badge with rounded corners
  standard,

  /// [GFBadgeShape.square], used for square badge
  square,
}
